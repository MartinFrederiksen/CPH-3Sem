import React from 'react';
import ReactDOM from 'react-dom';
import './index.css';
import App from './UseState';
import App2 from './DisplayTime';
import App3 from './Fetch';

let app = <App/>

const DontUseMeForReal = () => {
    return (
        <div className="App" onClick={handleSelect}>
         <a href="/"  className="x" id="app1">useState</a> &nbsp;
         <a href="/"  className="x" id="app2">displayTime</a> &nbsp;
         <a href="/"  className="x" id="app3">fetch</a> &nbsp;
         {/* Add as many as you have exercises, but remember className="x" */}
         {app}
        </div>
    )
}

function handleSelect(event) {
    event.preventDefault();
    if(event.target.className!=="x"){
      return
    }  
    const id = event.target.id;
    switch (id) {
        case "app1": app = <App />; break;
        case "app2": app = <App2 />; break;
        case "app3": app = <App3 />; break;
    }
    ReactDOM.render(<DontUseMeForReal />, document.getElementById('root'));
}

ReactDOM.render(<DontUseMeForReal />, document.getElementById('root'));


